        ud.setModel(model);
       // ud.setMaxseats(maxseats);
        ud.setMinseats(minseats);
        ud.setManuyear(manuyear); //check for error 1 might be there
        ud.setSerialno(serialno);
        ud.setLocation(location);
        ud.setTime(time);
        //ud.setDate_of_Upadte(date_of_Upadte);
