    private void lowbptxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_lowbptxtKeyPressed
        // TODO add your handling code here:
        char c =evt.getKeyChar();
if ((c >= '0' && c <= '9' )||(c == KeyEvent.VK_BACK_SPACE)||(c == KeyEvent.VK_DELETE)){
lowbptxt.setEditable(true);
}else{
lowbptxt.setEditable(false);
JOptionPane.showMessageDialog(null, "Please enter only numbers !");
}
    }//GEN-LAST:event_lowbptxtKeyPressed

