        Double time=Double.parseDouble(txtAvailabletime.getText());
        String carname=txtCarname.getText();
        boolean available=chkbxAvailYes.isSelected();
        boolean manucerti=chkbxlExpiredYes.isSelected();
       // Date dateofupdate = new Date(); //CHECK DATA TYPE & put try & catch for integer
        Date date_of_Upadte = new Date();
        
        UberDetails ud=history.addNewCars();
        
