    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Patient patient=this.sys.getPatientDirectory().getPatientList().get(drpdwnPersons.getSelectedIndex());
         Encounter encounter = new Encounter();
         VitalSigns vitals = new VitalSigns();
         vitals.setHighbloodPressure(Integer.parseInt(highbptxt.getText()));
         vitals.setLowbloodPressure(Integer.parseInt(lowbptxt.getText()));
         vitals.setHeartRate(Integer.parseInt(hearttxt.getText()));
         encounter.setVitalsigns(vitals);
        patient.getEncounterHistory().addEncounter(encounter);
        JOptionPane.showMessageDialog(this, "Encounter Added Successfully!");
        highbptxt.setText("");
        lowbptxt.setText("");
        hearttxt.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

